export const detectBrowserLanguage = (): string => {
  const browserLang = navigator.language.split('-')[0];
  return LANGUAGES.find(l => l.code === browserLang)?.code || 'en';
};

export const getTranslations = (langCode: string) => {
  return WEATHER_TRANSLATIONS[langCode as keyof typeof WEATHER_TRANSLATIONS] || WEATHER_TRANSLATIONS.en;
};

export const translateWeatherCondition = (condition: string, langCode: string): string => {
  const t = getTranslations(langCode);
  const conditionKey = condition.toLowerCase().replace(/\s+/g, '');
  
  const conditionMap: Record<string, keyof typeof WEATHER_TRANSLATIONS.en> = {
    'clear': 'clear',
    'sunny': 'sunny',
    'partlycloudy': 'partlyCloudy',
    'cloudy': 'cloudy',
    'overcast': 'overcast',
    'rain': 'rain',
    'lightrain': 'lightRain',
    'heavyrain': 'heavyRain',
    'drizzle': 'drizzle',
    'thunderstorm': 'thunderstorm',
    'snow': 'snow',
    'lightsnow': 'lightSnow',
    'heavysnow': 'heavySnow',
    'mist': 'mist',
    'fog': 'fog',
  };
  
  const translationKey = conditionMap[conditionKey];
  return translationKey ? t[translationKey] : condition;
};